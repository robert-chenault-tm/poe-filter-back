package chenaurj.poefilterback.poefilterback.model;


public class Map {

    private String corrupted;
    private String rarity;
    private String shaped;
    private String tier;
    private String quality;
    private Common common;

    public String getCorrupted() {
        return corrupted;
    }

    public void setCorrupted(String corrupted) {
        this.corrupted = corrupted;
    }

    public String getRarity() {
        return rarity;
    }

    public void setRarity(String rarity) {
        this.rarity = rarity;
    }

    public String getShaped() {
        return shaped;
    }

    public void setShaped(String shaped) {
        this.shaped = shaped;
    }

    public String getTier() {
        return tier;
    }

    public void setTier(String tier) {
        this.tier = tier;
    }

    public String getQuality() {
        return quality;
    }

    public void setQuality(String quality) {
        this.quality = quality;
    }

    public Common getCommon() {
        return common;
    }

    public void setCommon(Common common) {
        this.common = common;
    }
    
}