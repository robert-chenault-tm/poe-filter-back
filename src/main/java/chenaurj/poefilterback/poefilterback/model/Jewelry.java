package chenaurj.poefilterback.poefilterback.model;


public class Jewelry {

    private String corrupted;
    private String elder;
    private String fractured;
    private String identified;
    private String ilvl;
    private String rarity;
    private String shaper;
    private String synthesized;
    private String itemType;
    private Common common;

    public String getCorrupted() {
        return corrupted;
    }

    public void setCorrupted(String corrupted) {
        this.corrupted = corrupted;
    }

    public String getElder() {
        return elder;
    }

    public void setElder(String elder) {
        this.elder = elder;
    }

    public String getFractured() {
        return fractured;
    }

    public void setFractured(String fractured) {
        this.fractured = fractured;
    }

    public String getIdentified() {
        return identified;
    }

    public void setIdentified(String identified) {
        this.identified = identified;
    }

    public String getIlvl() {
        return ilvl;
    }

    public void setIlvl(String ilvl) {
        this.ilvl = ilvl;
    }

    public String getRarity() {
        return rarity;
    }

    public void setRarity(String rarity) {
        this.rarity = rarity;
    }

    public String getShaper() {
        return shaper;
    }

    public void setShaper(String shaper) {
        this.shaper = shaper;
    }

    public String getSynthesized() {
        return synthesized;
    }

    public void setSynthesized(String synthesized) {
        this.synthesized = synthesized;
    }

    public String getItemType() {
        return itemType;
    }

    public void setItemType(String itemType) {
        this.itemType = itemType;
    }

    public Common getCommon() {
        return common;
    }

    public void setCommon(Common common) {
        this.common = common;
    }

    
    
}